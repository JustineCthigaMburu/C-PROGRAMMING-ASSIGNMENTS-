#include <stdio.h>

int main() {
    int i; 
    int start; 
    int sum = 0;
    int stop;
     
    printf("Enter start value: ");
    scanf("%d", &start);  
    
    printf("Enter stop value: ");
    scanf("%d", &stop);
     
    for(i = start; i <= stop; i++) {
        printf("%d\n", i);
        sum += i;
    }
    
    printf("The sum is %d\n", sum);
     
    return 0;
}

     
